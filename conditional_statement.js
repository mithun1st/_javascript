//if
let num = -3;
if (0 < num) {
    console.log('Positive');
} else if (0 > num) {
    console.log('Negative');
} else {
    console.log('It\'s zero');
}


//switch
let color = 'g';
switch (color) {
    case 'r':
        console.log('red');
        break;
    case 'g':
        console.log('green');
        break;
    case 'b':
        console.log('blue');
        break;
    default:
        console.log('other color');
        break;
    
}